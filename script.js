function calculate() {
  const currency_one = currencyEl_one.value;
  const currency_two = currencyEl_two.value;
 
  const ccode =country_list[currency_one]
  const ccode2 =country_list[currency_two]


  document.getElementById("flagimg").src = `https://flagcdn.com/w80/${ccode.toLowerCase()}.png`;
  document.getElementById("flagimg2").src = `https://flagcdn.com/w80/${ccode2.toLowerCase()}.png`;

  //fetching exchange rate from  ExchangeRate Api

  fetch(`https://v6.exchangerate-api.com/v6/6a6de1ef74c88edaf72ef8bf/latest/${currency_one}`)
    .then((res) => res.json())
    .then((data) => {
        const rate = data.conversion_rates[currency_two];
        rateEl.innerText = ` Rate:  1 ${currency_one} = ${rate} ${currency_two}`;
        amountEl_two.value = (amountEl_one.value * rate).toFixed(2);
    });
}


// Event listening
currencyEl_one.addEventListener('change', calculate);
amountEl_one.addEventListener('input', calculate);
currencyEl_two.addEventListener('change', calculate);
amountEl_two.addEventListener('input', calculate);
swap.addEventListener('click', () => {
  const temp = currencyEl_one.value;
  currencyEl_one.value = currencyEl_two.value;
  currencyEl_two.value = temp;
  calculate();
});

calculate();
